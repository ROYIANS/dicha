import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import ts from 'typescript';

const modelSourceRoot = resolve(
  process.env.LOBEHUB_DIR ?? '../lobehub',
  'packages/model-bank/src/aiModels',
);
const providerSourceRoot = resolve(
  process.env.LOBEHUB_DIR ?? '../lobehub',
  'packages/model-bank/src/modelProviders',
);
const modelTargetPath = resolve('packages/shared/src/fixtures/lobehub-model-bank-data.ts');
const providerTargetPath = resolve('packages/shared/src/fixtures/lobehub-provider-data.ts');

const modelFiles = (await readdir(modelSourceRoot))
  .filter((file) => file.endsWith('.ts'))
  .filter((file) => file !== 'index.ts')
  .filter((file) => !file.endsWith('.test.ts'))
  .sort((left, right) => left.localeCompare(right));
const providerFiles = (await readdir(providerSourceRoot))
  .filter((file) => file.endsWith('.ts'))
  .filter((file) => file !== 'index.ts')
  .filter((file) => !file.endsWith('.test.ts'))
  .sort((left, right) => left.localeCompare(right));

const cards = [];
const providerCards = [];

for (const file of modelFiles) {
  const providerId = file.replace(/\.ts$/, '');
  const sourceText = await readFile(join(modelSourceRoot, file), 'utf8');
  const sourceFile = ts.createSourceFile(file, sourceText, ts.ScriptTarget.Latest, true, ts.ScriptKind.TS);
  cards.push(...collectModelCards(sourceFile).map((card) => ({ providerId, ...card })));
}

for (const file of providerFiles) {
  const lobeProviderId = file.replace(/\.ts$/, '');
  const sourceText = await readFile(join(providerSourceRoot, file), 'utf8');
  const sourceFile = ts.createSourceFile(file, sourceText, ts.ScriptTarget.Latest, true, ts.ScriptKind.TS);
  const providerCard = collectProviderCard(sourceFile);
  if (providerCard) providerCards.push({ lobeProviderId, ...providerCard });
}

const modelOutput = `// Generated by packages/shared/scripts/sync-lobehub-model-bank.mjs.
// Source: LobeHub packages/model-bank/src/aiModels
// Do not edit by hand.
import type { LobeModelBankCard } from './lobehub-model-bank';

export const lobeModelBankCards = JSON.parse(${JSON.stringify(JSON.stringify(cards))}) as LobeModelBankCard[];
`;

const providerOutput = `// Generated by packages/shared/scripts/sync-lobehub-model-bank.mjs.
// Source: LobeHub packages/model-bank/src/modelProviders
// Do not edit by hand.
import type { LobeProviderCard } from './lobehub-model-bank';

export const lobeProviderCards = JSON.parse(${JSON.stringify(JSON.stringify(providerCards))}) as LobeProviderCard[];
`;

await mkdir(dirname(modelTargetPath), { recursive: true });
await writeFile(modelTargetPath, modelOutput, 'utf8');
await writeFile(providerTargetPath, providerOutput, 'utf8');

const providers = new Set(cards.map((card) => card.providerId));
console.log(
  `Generated ${cards.length} LobeHub model-bank cards from ${providers.size} providers and ${providerCards.length} provider cards.`,
);

function collectModelCards(sourceFile) {
  const models = [];

  const visit = (node) => {
    if (ts.isObjectLiteralExpression(node)) {
      const value = literalValue(node);
      if (value && typeof value.id === 'string' && typeof value.type === 'string') {
        models.push(value);
      }
    }
    ts.forEachChild(node, visit);
  };

  visit(sourceFile);
  return models;
}

function collectProviderCard(sourceFile) {
  let provider;

  const visit = (node) => {
    if (ts.isObjectLiteralExpression(node)) {
      const value = literalValue(node);
      if (
        value &&
        typeof value.id === 'string' &&
        typeof value.name === 'string' &&
        typeof value.url === 'string'
      ) {
        provider = value;
      }
    }
    ts.forEachChild(node, visit);
  };

  visit(sourceFile);
  return provider;
}

function literalValue(node) {
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) return node.text;
  if (ts.isNumericLiteral(node)) return Number(node.text.replaceAll('_', ''));
  if (node.kind === ts.SyntaxKind.TrueKeyword) return true;
  if (node.kind === ts.SyntaxKind.FalseKeyword) return false;
  if (node.kind === ts.SyntaxKind.NullKeyword) return null;

  if (ts.isPrefixUnaryExpression(node) && node.operator === ts.SyntaxKind.MinusToken) {
    const value = literalValue(node.operand);
    return typeof value === 'number' ? -value : undefined;
  }

  if (ts.isParenthesizedExpression(node)) return literalValue(node.expression);

  if (ts.isBinaryExpression(node)) {
    const left = literalValue(node.left);
    const right = literalValue(node.right);
    if (typeof left !== 'number' || typeof right !== 'number') return undefined;
    if (node.operatorToken.kind === ts.SyntaxKind.AsteriskToken) return left * right;
    if (node.operatorToken.kind === ts.SyntaxKind.SlashToken) return left / right;
    if (node.operatorToken.kind === ts.SyntaxKind.PlusToken) return left + right;
    if (node.operatorToken.kind === ts.SyntaxKind.MinusToken) return left - right;
    return undefined;
  }

  if (ts.isArrayLiteralExpression(node)) {
    return node.elements.map((element) => literalValue(element)).filter((value) => value !== undefined);
  }

  if (ts.isObjectLiteralExpression(node)) {
    const object = {};
    for (const property of node.properties) {
      if (!ts.isPropertyAssignment(property)) continue;
      const name = propertyName(property.name);
      if (!name) continue;
      const value = literalValue(property.initializer);
      if (value !== undefined) object[name] = value;
    }
    return object;
  }

  return undefined;
}

function propertyName(name) {
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNumericLiteral(name)) return name.text;
  return undefined;
}
